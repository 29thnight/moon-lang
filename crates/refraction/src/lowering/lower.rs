/// Like `lower_func_member` but routes the body through `lower_stmts_with_ctx`
/// so that `listen`/`unlisten` inside user functions resolve against the
/// component-level `ComponentCtx`.
fn lower_func_member_with_ctx(m: &Member, ctx: &mut ComponentCtx) -> CsMember {
    match m {
        Member::Func { visibility, is_static, is_override, is_abstract, is_open, is_async, name, type_params, where_clauses, params, return_ty, body, .. } => {
            let inner_ret = return_ty.as_ref().map(|t| lower_type(t)).unwrap_or("void".into());
            let ret = if *is_async {
                async_return_type(&inner_ret)
            } else {
                inner_ret.clone()
            };
            let ps: Vec<CsParam> = params.iter().map(cs_param_from).collect();

            let mut mods = lower_visibility(*visibility);
            if *is_static {
                mods = format!("{} static", mods);
            }
            if *is_async {
                mods = format!("{} async", mods);
            }
            if *is_abstract {
                mods = format!("{} abstract", mods);
            } else if *is_open {
                mods = format!("{} virtual", mods);
            } else if *is_override {
                mods = format!("{} override", mods);
            }

            // Abstract functions emit signature only (no body)
            if *is_abstract {
                let cs_name = lower_func_name_with_generics(name, type_params);
                return CsMember::RawCode(format!(
                    "{} {} {}({});",
                    mods,
                    ret,
                    cs_name,
                    ps.iter().map(|p| format!("{}{} {}", p.modifier.prefix(), p.ty, p.name)).collect::<Vec<_>>().join(", ")
                ));
            }

            let cs_body = match body {
                FuncBody::Block(block) => {
                    lower_stmts_with_ctx(&block.stmts, ctx, return_ty.as_ref())
                }
                FuncBody::ExprBody(expr) => {
                    vec![CsStmt::Return(
                        Some(lower_expr_with_expected_type(expr, return_ty.as_ref(), Some(&ctx.callable_signatures))),
                        Some(expr_span(expr)),
                    )]
                }
            };

            // Build method name with optional type parameters
            let cs_name = lower_func_name_with_generics(name, type_params);

            CsMember::Method {
                attributes: vec![],
                modifiers: mods,
                return_ty: ret,
                name: cs_name,
                params: ps,
                where_clauses: lower_where_clauses(where_clauses),
                body: cs_body,
                source_span: Some(member_span(m)),
            }
        }
        _ => CsMember::RawCode("// unexpected member".into()),
    }
}

// ── Component-aware coroutine lowering ─────────────────────────

/// Like `lower_coroutine` but routes through `lower_stmts_with_ctx`.
fn lower_coroutine_with_ctx(
    name: &str,
    params: &[Param],
    body: &Block,
    return_ty: Option<&TypeRef>,
    ctx: &mut ComponentCtx,
) -> CsMember {
    let ps: Vec<CsParam> = params.iter().map(cs_param_from).collect();

    let cs_body = lower_stmts_with_ctx(&body.stmts, ctx, None);

    // Language 5, Sprint 1: choose the lowered return type from an
    // explicit `: Seq<T>` / `: IEnumerator(<T>)` / `: IEnumerable(<T>)`
    // annotation. Falls back to the v4 default of `IEnumerator`.
    let cs_return_ty = match return_ty {
        Some(ty) => lower_coroutine_return_type(ty),
        None => "System.Collections.IEnumerator".into(),
    };

    CsMember::Method {
        attributes: vec![],
        modifiers: "private".into(),
        return_ty: cs_return_ty,
        name: name.into(),
        params: ps,
        where_clauses: vec![],
        body: cs_body,
        source_span: Some(body.span),
    }
}

/// Lower a coroutine return type annotation. `Seq<T>` becomes
/// `System.Collections.Generic.IEnumerator<T>` and the C# `IEnumerator(<T>)`
/// / `IEnumerable(<T>)` family is forwarded as-is. Anything else falls
/// back to the non-generic `IEnumerator` and lets the user catch the
/// mismatch via E148 from the analyzer.
fn lower_coroutine_return_type(ty: &TypeRef) -> String {
    match ty {
        TypeRef::Generic { name, type_args, .. } if name == "Seq" && type_args.len() == 1 => {
            format!(
                "System.Collections.Generic.IEnumerator<{}>",
                lower_type(&type_args[0])
            )
        }
        TypeRef::Generic { name, type_args, .. } if name == "IEnumerator" && type_args.len() == 1 => {
            format!(
                "System.Collections.Generic.IEnumerator<{}>",
                lower_type(&type_args[0])
            )
        }
        TypeRef::Generic { name, type_args, .. } if name == "IEnumerable" && type_args.len() == 1 => {
            format!(
                "System.Collections.Generic.IEnumerable<{}>",
                lower_type(&type_args[0])
            )
        }
        TypeRef::Simple { name, .. } if name == "IEnumerator" => "System.Collections.IEnumerator".into(),
        TypeRef::Simple { name, .. } if name == "IEnumerable" => "System.Collections.IEnumerable".into(),
        _ => "System.Collections.IEnumerator".into(),
    }
}

