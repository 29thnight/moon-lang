//! Lowering pass: PrSM AST -> C# IR.

use crate::ast::*;
use crate::lexer::token::Span;
use super::csharp_ir::*;
use std::collections::HashMap;

/// Lower a PrSM file to C# IR.
pub fn lower_file(file: &File) -> CsFile {
    let usings = lower_usings(&file.usings);
    let (class, extra_types) = lower_decl(&file.decl);

    CsFile {
        header_comment: "// <auto-generated>\n// This file was generated by the refraction compiler. Do not edit manually.\n// </auto-generated>".into(),
        usings,
        class,
        extra_types,
    }
}

fn lower_usings(usings: &[UsingDecl]) -> Vec<String> {
    let mut result: Vec<String> = usings.iter().map(|u| u.path.clone()).collect();
    // Ensure System.Collections is present (for IEnumerator)
    if !result.iter().any(|u| u == "System.Collections") {
        result.push("System.Collections".into());
    }
    result
}

fn lower_decl(decl: &Decl) -> (CsClass, Vec<CsClass>) {
    match decl {
        Decl::Component { name, is_singleton, base_class, interfaces, members, .. } => {
            let mut cs_members = Vec::new();
            let callable_signatures = collect_callable_signatures(members);
            let (require_fields, optional_fields, child_fields, parent_fields, pool_fields, user_awake) =
                collect_component_init(members);

            // Lower fields first
            for m in members {
                match m {
                    Member::SerializeField { .. } | Member::Field { .. } => {
                        cs_members.extend(lower_field_member(m));
                    }
                    Member::Require { name, ty, .. }
                    | Member::Optional { name, ty, .. }
                    | Member::Child { name, ty, .. }
                    | Member::Parent { name, ty, .. } => {
                        let cs_ty = lower_type(ty);
                        let backing = format!("_{}", name);
                        cs_members.push(CsMember::Field {
                            attributes: vec![],
                            modifiers: "private".into(),
                            ty: cs_ty.clone(),
                            name: backing.clone(),
                            init: None,
                        });
                        cs_members.push(CsMember::Property {
                            modifiers: "public".into(),
                            ty: cs_ty,
                            name: name.clone(),
                            getter_expr: backing,
                            setter: None,
                            setter_expr: None,
                        });
                    }
                    Member::Pool { name, item_type, .. } => {
                        let item_ty = lower_type(item_type);
                        let pool_ty = format!("ObjectPool<{}>", item_ty);
                        let backing = format!("_{}", name);
                        cs_members.push(CsMember::Field {
                            attributes: vec![],
                            modifiers: "private".into(),
                            ty: pool_ty.clone(),
                            name: backing.clone(),
                            init: None,
                        });
                        cs_members.push(CsMember::Property {
                            modifiers: "public".into(),
                            ty: pool_ty,
                            name: name.clone(),
                            getter_expr: backing,
                            setter: None,
                            setter_expr: None,
                        });
                    }
                    _ => {}
                }
            }

            // Generate Awake method (require/optional resolution + pool init + user awake)
            if !require_fields.is_empty() || !optional_fields.is_empty()
                || !child_fields.is_empty() || !parent_fields.is_empty()
                || !pool_fields.is_empty()
                || user_awake.is_some()
            {
                cs_members.push(lower_awake(
                    name, &require_fields, &optional_fields,
                    &child_fields, &parent_fields, &pool_fields, user_awake, &callable_signatures,
                ));
            }

            // Lower remaining members — use ComponentCtx to collect listen subscriptions.
            let mut listen_ctx = ComponentCtx::new(callable_signatures.clone());
            for m in members {
                match m {
                    Member::Lifecycle { kind, params, body, .. } if *kind != LifecycleKind::Awake => {
                        cs_members.push(lower_lifecycle_with_ctx(*kind, params, body, &mut listen_ctx));
                    }
                    Member::Func { .. } => {
                        cs_members.push(lower_func_member_with_ctx(m, &mut listen_ctx));
                    }
                    Member::Coroutine { name: cname, params, body, .. } => {
                        cs_members.push(lower_coroutine_with_ctx(cname, params, body, &mut listen_ctx));
                    }
                    Member::IntrinsicFunc { visibility, name: fname, params, return_ty, code, span, .. } => {
                        cs_members.push(lower_intrinsic_func(*visibility, fname, params, return_ty.as_ref(), code, *span));
                    }
                    Member::IntrinsicCoroutine { name: cname, params, code, span, .. } => {
                        cs_members.push(lower_intrinsic_coroutine(cname, params, code, *span));
                    }
                    _ => {}
                }
            }

            // Emit private handler fields and cleanup methods if any v2 listen stmts were found.
            if !listen_ctx.records.is_empty() {
                // Insert private handler fields right before the methods (after existing fields).
                let field_insert_pos = cs_members.iter().position(|m| matches!(m, CsMember::Method { .. } | CsMember::Property { .. })).unwrap_or(cs_members.len());
                let fields = subscription_fields(&listen_ctx.records);
                for (i, f) in fields.into_iter().enumerate() {
                    cs_members.insert(field_insert_pos + i, f);
                }
                // Inject cleanup calls into OnDisable / OnDestroy (or synthesize).
                inject_cleanup_calls(&mut cs_members, &listen_ctx.records);
                // Append cleanup implementation methods.
                cs_members.extend(cleanup_methods(&listen_ctx.records));
            }

            // v2 New Input System sugar: if any method uses input.action(...), inject
            // a `private PlayerInput _prsmInput;` field and `_prsmInput = GetComponent<PlayerInput>()`
            // in Awake.
            let uses_input_system = members.iter().any(member_uses_new_input);
            if uses_input_system {
                // Insert the PlayerInput backing field before the first method/property.
                let field_pos = cs_members.iter().position(|m| matches!(m, CsMember::Method { .. } | CsMember::Property { .. })).unwrap_or(cs_members.len());
                cs_members.insert(field_pos, CsMember::Field {
                    attributes: vec![],
                    modifiers: "private".into(),
                    ty: "PlayerInput".into(),
                    name: PRSM_INPUT_FIELD.into(),
                    init: None,
                });
                // Inject `_prsmInput = GetComponent<PlayerInput>();` into Awake.
                inject_or_synthesize(
                    &mut cs_members,
                    "Awake",
                    &format!("{} = GetComponent<PlayerInput>();", PRSM_INPUT_FIELD),
                );
            }

            // Singleton pattern: inject _instance field, Instance property, and Awake guard.
            if *is_singleton {
                // Insert _instance field and Instance property at the top of the member list.
                let instance_field = CsMember::Field {
                    attributes: vec![],
                    modifiers: "private static".into(),
                    ty: name.clone(),
                    name: "_instance".into(),
                    init: None,
                };
                let instance_property = CsMember::RawCode(format!(
                    concat!(
                        "public static {0} Instance\n",
                        "    {{\n",
                        "        get\n",
                        "        {{\n",
                        "            if (_instance == null)\n",
                        "            {{\n",
                        "                _instance = FindFirstObjectByType<{0}>();\n",
                        "                if (_instance == null)\n",
                        "                {{\n",
                        "                    var go = new GameObject(nameof({0}));\n",
                        "                    _instance = go.AddComponent<{0}>();\n",
                        "                }}\n",
                        "            }}\n",
                        "            return _instance;\n",
                        "        }}\n",
                        "    }}",
                    ),
                    name
                ));
                cs_members.insert(0, instance_property);
                cs_members.insert(0, instance_field);

                // Inject singleton guard into Awake (prepended before all other Awake code).
                let singleton_awake_guard = concat!(
                    "if (_instance == null)\n",
                    "        {\n",
                    "            _instance = this;\n",
                    "            DontDestroyOnLoad(gameObject);\n",
                    "        }\n",
                    "        else if (_instance != this)\n",
                    "        {\n",
                    "            Destroy(gameObject);\n",
                    "            return;\n",
                    "        }",
                );
                inject_or_synthesize(&mut cs_members, "Awake", singleton_awake_guard);
            }

            if needs_expr_helper(&cs_members) {
                cs_members.push(lower_expr_helper_member());
            }

            (
                CsClass {
                    attributes: vec![],
                    modifiers: "public".into(),
                    name: name.clone(),
                    base_class: Some(base_class.clone()),
                    interfaces: interfaces.clone(),
                    where_clauses: vec![],
                    members: cs_members,
                },
                vec![],
            )
        }
        Decl::Asset { name, base_class, members, .. } => {
            let mut cs_members = Vec::new();
            let callable_signatures = collect_callable_signatures(members);
            for m in members {
                match m {
                    Member::SerializeField { .. } | Member::Field { .. } => {
                        cs_members.extend(lower_field_member(m));
                    }
                    Member::Func { .. } => {
                        cs_members.push(lower_func_member(m, &callable_signatures));
                    }
                    Member::IntrinsicFunc { visibility, name: fname, params, return_ty, code, span, .. } => {
                        cs_members.push(lower_intrinsic_func(*visibility, fname, params, return_ty.as_ref(), code, *span));
                    }
                    Member::IntrinsicCoroutine { name: cname, params, code, span, .. } => {
                        cs_members.push(lower_intrinsic_coroutine(cname, params, code, *span));
                    }
                    _ => {}
                }
            }
            if needs_expr_helper(&cs_members) {
                cs_members.push(lower_expr_helper_member());
            }
            (
                CsClass {
                    attributes: vec![format!("[CreateAssetMenu(fileName = \"New {name}\", menuName = \"PrSM/{name}\")]")],
                    modifiers: "public".into(),
                    name: name.clone(),
                    base_class: Some(base_class.clone()),
                    interfaces: vec![],
                    where_clauses: vec![],
                    members: cs_members,
                },
                vec![],
            )
        }
        Decl::Class { name, type_params, where_clauses, super_class, interfaces, members, .. } => {
            let mut cs_members = Vec::new();
            let callable_signatures = collect_callable_signatures(members);
            for m in members {
                match m {
                    Member::Field { .. } => cs_members.extend(lower_field_member(m)),
                    Member::Func { .. } => cs_members.push(lower_func_member(m, &callable_signatures)),
                    Member::IntrinsicFunc { visibility, name: fname, params, return_ty, code, span, .. } => {
                        cs_members.push(lower_intrinsic_func(*visibility, fname, params, return_ty.as_ref(), code, *span));
                    }
                    Member::IntrinsicCoroutine { name: cname, params, code, span, .. } => {
                        cs_members.push(lower_intrinsic_coroutine(cname, params, code, *span));
                    }
                    _ => {}
                }
            }
            if needs_expr_helper(&cs_members) {
                cs_members.push(lower_expr_helper_member());
            }
            // Build class name with optional type parameters
            let cs_name = if type_params.is_empty() {
                name.clone()
            } else {
                format!("{}<{}>", name, type_params.join(", "))
            };
            (
                CsClass {
                    attributes: vec![],
                    modifiers: "public".into(),
                    name: cs_name,
                    base_class: super_class.clone(),
                    interfaces: interfaces.clone(),
                    where_clauses: lower_where_clauses(where_clauses),
                    members: cs_members,
                },
                vec![],
            )
        }
        Decl::DataClass { name, fields, .. } => {
            (lower_data_class(name, fields), vec![])
        }
        Decl::Enum { name, params, entries, .. } => {
            let mut cs_members = Vec::new();
            for entry in entries {
                cs_members.push(CsMember::RawCode(format!("{},", entry.name)));
            }
            let enum_class = CsClass {
                attributes: vec![],
                modifiers: "public enum".into(),
                name: name.clone(),
                base_class: None,
                interfaces: vec![],
                where_clauses: vec![],
                members: cs_members,
            };
            let extra_types = if params.is_empty() {
                vec![]
            } else {
                vec![lower_parameterized_enum_extensions(name, params, entries)]
            };
            (enum_class, extra_types)
        }
        Decl::Interface { name, extends, members, .. } => {
            let mut cs_members = Vec::new();
            for member in members {
                match member {
                    InterfaceMember::Func { name: fn_name, params, return_ty, .. } => {
                        let ret = return_ty.as_ref().map(|t| lower_type(t)).unwrap_or_else(|| "void".into());
                        let ps: String = params.iter().map(|p| format!("{} {}", lower_type(&p.ty), p.name)).collect::<Vec<_>>().join(", ");
                        cs_members.push(CsMember::RawCode(format!("{} {}({});", ret, fn_name, ps)));
                    }
                    InterfaceMember::Property { name: prop_name, ty, mutable, .. } => {
                        let cs_ty = lower_type(ty);
                        if *mutable {
                            cs_members.push(CsMember::RawCode(format!("{} {} {{ get; set; }}", cs_ty, prop_name)));
                        } else {
                            cs_members.push(CsMember::RawCode(format!("{} {} {{ get; }}", cs_ty, prop_name)));
                        }
                    }
                }
            }
            (
                CsClass {
                    attributes: vec![],
                    modifiers: "public interface".into(),
                    name: name.clone(),
                    base_class: None,
                    interfaces: extends.clone(),
                    where_clauses: vec![],
                    members: cs_members,
                },
                vec![],
            )
        }
        Decl::Attribute { name, fields, targets, .. } => {
            // attribute Foo(val x: Int) : Method, Property
            // → [AttributeUsage(AttributeTargets.Method | AttributeTargets.Property)]
            //   public class FooAttribute : System.Attribute { ... }
            let mut cs_members = Vec::new();
            let mut ctor_params = Vec::new();
            let mut ctor_body = Vec::new();
            for f in fields {
                let cs_ty = lower_type(&f.ty);
                cs_members.push(CsMember::Field {
                    attributes: vec![],
                    modifiers: "public".into(),
                    ty: cs_ty.clone(),
                    name: f.name.clone(),
                    init: None,
                });
                ctor_params.push(CsParam {
                    ty: cs_ty.clone(),
                    name: f.name.clone(),
                    default: None,
                });
                ctor_body.push(CsStmt::Assignment {
                    target: format!("this.{}", f.name),
                    op: "=".into(),
                    value: f.name.clone(),
                    source_span: None,
                });
            }
            cs_members.push(CsMember::Method {
                attributes: vec![],
                modifiers: "public".into(),
                return_ty: String::new(), // constructor — no return type
                name: format!("{}Attribute", name),
                params: ctor_params,
                where_clauses: vec![],
                body: ctor_body,
                source_span: None,
            });
            let usage = if targets.is_empty() {
                "System.AttributeTargets.All".to_string()
            } else {
                targets.iter()
                    .map(|t| format!("System.AttributeTargets.{}", t))
                    .collect::<Vec<_>>()
                    .join(" | ")
            };
            (
                CsClass {
                    attributes: vec![format!("[System.AttributeUsage({})]", usage)],
                    modifiers: "public".into(),
                    name: format!("{}Attribute", name),
                    base_class: Some("System.Attribute".into()),
                    interfaces: vec![],
                    where_clauses: vec![],
                    members: cs_members,
                },
                vec![],
            )
        }
        Decl::TypeAlias { name, target, .. } => {
            // typealias lowers to a C# `using Name = FullType;` directive.
            // We emit an empty class with a using alias marker.
            // The using is added by the caller via extra_types.
            (
                CsClass {
                    attributes: vec![],
                    modifiers: String::new(),
                    name: format!("__typealias_{}", name),
                    base_class: None,
                    interfaces: vec![],
                    where_clauses: vec![],
                    members: vec![CsMember::RawCode(format!("// typealias {} = {}", name, lower_type(target)))],
                },
                vec![],
            )
        }
    }
}

fn lower_data_class(name: &str, fields: &[Param]) -> CsClass {
    let mut cs_members = Vec::new();

    for field in fields {
        cs_members.push(CsMember::Field {
            attributes: vec![],
            modifiers: "public".into(),
            ty: lower_type(&field.ty),
            name: field.name.clone(),
            init: None,
        });
    }

    cs_members.push(lower_data_class_constructor(name, fields));
    cs_members.push(lower_data_class_equals(name, fields));
    cs_members.push(lower_data_class_hash_code(fields));
    cs_members.push(lower_data_class_to_string(name, fields));
    if needs_expr_helper(&cs_members) {
        cs_members.push(lower_expr_helper_member());
    }

    CsClass {
        attributes: vec!["[System.Serializable]".into()],
        modifiers: "public".into(),
        name: name.into(),
        base_class: None,
        interfaces: vec![],
        where_clauses: vec![],
        members: cs_members,
    }
}

fn lower_expr_helper_member() -> CsMember {
    CsMember::Method {
        attributes: vec![],
        modifiers: "private static".into(),
        return_ty: "T".into(),
        name: "__prsm_expr<T>".into(),
        params: vec![CsParam {
            ty: "System.Func<T>".into(),
            name: "thunk".into(),
            default: None,
        }],
        where_clauses: vec![],
        body: vec![CsStmt::Return(Some("thunk()".into()), None)],
        source_span: None,
    }
}

/// Returns true if any member references `__prsm_expr` (i.e. uses a block expression).
/// Used to conditionally emit the helper method only when needed.
fn needs_expr_helper(members: &[CsMember]) -> bool {
    members.iter().any(|m| member_uses_expr_helper(m))
}

fn member_uses_expr_helper(member: &CsMember) -> bool {
    match member {
        CsMember::Method { body, .. } => body.iter().any(|s| stmt_uses_expr_helper(s)),
        _ => false,
    }
}

fn stmt_uses_expr_helper(stmt: &CsStmt) -> bool {
    match stmt {
        CsStmt::Expr(s, _) | CsStmt::Raw(s, _) => s.contains("__prsm_expr"),
        CsStmt::Return(Some(s), _) | CsStmt::YieldReturn(s, _) => s.contains("__prsm_expr"),
        CsStmt::VarDecl { init, .. } => init.contains("__prsm_expr"),
        CsStmt::Assignment { value, .. } => value.contains("__prsm_expr"),
        CsStmt::If { cond, then_body, else_body, .. } => {
            cond.contains("__prsm_expr")
                || then_body.iter().any(|s| stmt_uses_expr_helper(s))
                || else_body.as_ref().map_or(false, |b| b.iter().any(|s| stmt_uses_expr_helper(s)))
        }
        CsStmt::Switch { subject, cases, .. } => {
            subject.contains("__prsm_expr")
                || cases.iter().any(|c| c.body.iter().any(|s| stmt_uses_expr_helper(s)))
        }
        CsStmt::For { init, cond, incr, body, .. } => {
            init.contains("__prsm_expr")
                || cond.contains("__prsm_expr")
                || incr.contains("__prsm_expr")
                || body.iter().any(|s| stmt_uses_expr_helper(s))
        }
        CsStmt::ForEach { iterable, body, .. } => {
            iterable.contains("__prsm_expr")
                || body.iter().any(|s| stmt_uses_expr_helper(s))
        }
        CsStmt::While { cond, body, .. } => {
            cond.contains("__prsm_expr")
                || body.iter().any(|s| stmt_uses_expr_helper(s))
        }
        CsStmt::Block(stmts, _) => stmts.iter().any(|s| stmt_uses_expr_helper(s)),
        _ => false,
    }
}

fn lower_data_class_constructor(name: &str, fields: &[Param]) -> CsMember {
    let params = fields
        .iter()
        .map(|field| CsParam {
            ty: lower_type(&field.ty),
            name: field.name.clone(),
            default: None,
        })
        .collect();

    let body = fields
        .iter()
        .map(|field| CsStmt::Assignment {
            target: format!("this.{}", field.name),
            op: "=".into(),
            value: field.name.clone(),
            source_span: None,
        })
        .collect();

    CsMember::Method {
        attributes: vec![],
        modifiers: "public".into(),
        return_ty: String::new(),
        name: name.into(),
        params,
        where_clauses: vec![],
        body,
        source_span: None,
    }
}

fn lower_data_class_equals(name: &str, fields: &[Param]) -> CsMember {
    let equality_expr = if fields.is_empty() {
        "true".into()
    } else {
        fields
            .iter()
            .map(|field| {
                format!(
                    "System.Collections.Generic.EqualityComparer<{}>.Default.Equals({}, other.{})",
                    lower_type(&field.ty),
                    field.name,
                    field.name,
                )
            })
            .collect::<Vec<_>>()
            .join(" && ")
    };

    CsMember::Method {
        attributes: vec![],
        modifiers: "public override".into(),
        return_ty: "bool".into(),
        name: "Equals".into(),
        params: vec![CsParam {
            ty: "object".into(),
            name: "obj".into(),
            default: None,
        }],
        where_clauses: vec![],
        body: vec![
            CsStmt::If {
                cond: "ReferenceEquals(null, obj)".into(),
                then_body: vec![CsStmt::Return(Some("false".into()), None)],
                else_body: None,
                source_span: None,
            },
            CsStmt::If {
                cond: "ReferenceEquals(this, obj)".into(),
                then_body: vec![CsStmt::Return(Some("true".into()), None)],
                else_body: None,
                source_span: None,
            },
            CsStmt::If {
                cond: "obj.GetType() != GetType()".into(),
                then_body: vec![CsStmt::Return(Some("false".into()), None)],
                else_body: None,
                source_span: None,
            },
            CsStmt::VarDecl {
                ty: "var".into(),
                name: "other".into(),
                init: format!("({})obj", name),
                source_span: None,
            },
            CsStmt::Return(Some(equality_expr), None),
        ],
        source_span: None,
    }
}

fn lower_data_class_hash_code(fields: &[Param]) -> CsMember {
    let body = if fields.is_empty() {
        vec![CsStmt::Return(Some("17".into()), None)]
    } else {
        let mut lines = vec![
            "unchecked".to_string(),
            "{".to_string(),
            "    var hash = 17;".to_string(),
        ];
        for field in fields {
            lines.push(format!(
                "    hash = (hash * 31) + System.Collections.Generic.EqualityComparer<{}>.Default.GetHashCode({});",
                lower_type(&field.ty),
                field.name,
            ));
        }
        lines.push("    return hash;".to_string());
        lines.push("}".to_string());
        vec![CsStmt::Raw(lines.join("\n"), None)]
    };

    CsMember::Method {
        attributes: vec![],
        modifiers: "public override".into(),
        return_ty: "int".into(),
        name: "GetHashCode".into(),
        params: vec![],
        where_clauses: vec![],
        body,
        source_span: None,
    }
}

fn lower_data_class_to_string(name: &str, fields: &[Param]) -> CsMember {
    let body_expr = if fields.is_empty() {
        format!("$\"{}()\"", name)
    } else {
        let fields_display = fields
            .iter()
            .map(|field| format!("{}={{{}}}", field.name, field.name))
            .collect::<Vec<_>>()
            .join(", ");
        format!("$\"{}({})\"", name, fields_display)
    };

    CsMember::Method {
        attributes: vec![],
        modifiers: "public override".into(),
        return_ty: "string".into(),
        name: "ToString".into(),
        params: vec![],
        where_clauses: vec![],
        body: vec![CsStmt::Return(Some(body_expr), None)],
        source_span: None,
    }
}

fn lower_parameterized_enum_extensions(name: &str, params: &[EnumParam], entries: &[EnumEntry]) -> CsClass {
    let mut members = Vec::new();

    for (index, param) in params.iter().enumerate() {
        let mut cases = Vec::new();
        for entry in entries {
            cases.push(CsSwitchCase {
                pattern: format!("case {}.{}:", name, entry.name),
                body: vec![CsStmt::Return(Some(
                    entry
                        .args
                        .get(index)
                        .map(lower_expr)
                        .unwrap_or_else(|| "default".into()),
                ), None)],
            });
        }
        cases.push(CsSwitchCase {
            pattern: "default:".into(),
            body: vec![CsStmt::Raw(format!(
                "throw new System.InvalidOperationException($\"Unknown {} value: {{value}}\");",
                name,
            ), None)],
        });

        members.push(CsMember::Method {
            attributes: vec![],
            modifiers: "public static".into(),
            return_ty: lower_type(&param.ty),
            name: pascal_case(&param.name),
            params: vec![CsParam {
                ty: format!("this {}", name),
                name: "value".into(),
                default: None,
            }],
            where_clauses: vec![],
            body: vec![CsStmt::Switch {
                subject: "value".into(),
                cases,
                source_span: None,
            }],
            source_span: None,
        });
    }

    CsClass {
        attributes: vec![],
        modifiers: "public static".into(),
        name: format!("{}Extensions", name),
        base_class: None,
        interfaces: vec![],
        where_clauses: vec![],
        members,
    }
}

// ── Field lowering ──────────────────────────────────────────────

/// Check if annotations contain @field
fn has_field_annotation(annotations: &[Annotation]) -> bool {
    annotations.iter().any(|a| a.name == "field")
}

fn lower_field_member(m: &Member) -> Vec<CsMember> {
    match m {
        Member::SerializeField { annotations, visibility, mutability, name, ty, init, .. } => {
            let cs_ty = lower_type(ty);
            let init_str = init.as_ref().map(|e| lower_expr(e));
            let getter_vis = match visibility {
                Some(v) => lower_visibility(*v),
                None => "public".into(),
            };

            // @field → direct public field, no backing+property
            if has_field_annotation(annotations) {
                let mut attrs = vec!["[SerializeField]".into()];
                for ann in annotations {
                    if ann.name != "field" {
                        attrs.push(lower_annotation(ann));
                    }
                }
                return vec![CsMember::Field {
                    attributes: attrs,
                    modifiers: getter_vis,
                    ty: cs_ty,
                    name: name.clone(),
                    init: init_str,
                }];
            }

            let backing = format!("_{}", name);
            let mut attrs = vec!["[SerializeField]".into()];
            for ann in annotations {
                attrs.push(lower_annotation(ann));
            }

            let mut result = vec![
                CsMember::Field {
                    attributes: attrs,
                    modifiers: "private".into(),
                    ty: cs_ty.clone(),
                    name: backing.clone(),
                    init: init_str,
                },
            ];

            match mutability {
                Mutability::Var => {
                    result.push(CsMember::Property {
                        modifiers: getter_vis,
                        ty: cs_ty,
                        name: name.clone(),
                        getter_expr: backing.clone(),
                        setter: Some("set".into()),
                        setter_expr: Some(backing),
                    });
                }
                _ => {
                    result.push(CsMember::Property {
                        modifiers: getter_vis,
                        ty: cs_ty,
                        name: name.clone(),
                        getter_expr: backing,
                        setter: None,
                        setter_expr: None,
                    });
                }
            }
            result
        }
        Member::Field { visibility, is_static, mutability, name, ty, init, .. } => {
            let cs_ty = ty.as_ref().map(|t| lower_type(t)).unwrap_or("var".into());
            let init_str = init.as_ref().map(|e| lower_expr(e));
            let vis = lower_visibility(*visibility);
            let static_prefix = if *is_static { "static " } else { "" };

            // const → C# const (no backing field pattern)
            if *mutability == Mutability::Const {
                return vec![CsMember::Field {
                    attributes: vec![],
                    modifiers: format!("{} {}const", vis, static_prefix).trim().to_string(),
                    ty: cs_ty,
                    name: name.clone(),
                    init: init_str,
                }];
            }

            // static val → C# static readonly field (no backing+property pattern)
            if *is_static {
                let readonly_mod = if *mutability == Mutability::Val || *mutability == Mutability::Fixed { "readonly " } else { "" };
                return vec![CsMember::Field {
                    attributes: vec![],
                    modifiers: format!("{} static {}{}", vis, readonly_mod, "").trim().to_string(),
                    ty: cs_ty,
                    name: name.clone(),
                    init: init_str,
                }];
            }

            let backing = format!("_{}", name);
            let getter_vis = vis;

            let mut result = vec![
                CsMember::Field {
                    attributes: vec![],
                    modifiers: "private".into(),
                    ty: cs_ty.clone(),
                    name: backing.clone(),
                    init: init_str,
                },
            ];

            match mutability {
                Mutability::Var => {
                    result.push(CsMember::Property {
                        modifiers: getter_vis,
                        ty: cs_ty,
                        name: name.clone(),
                        getter_expr: backing.clone(),
                        setter: Some("set".into()),
                        setter_expr: Some(backing),
                    });
                }
                _ => {
                    result.push(CsMember::Property {
                        modifiers: getter_vis,
                        ty: cs_ty,
                        name: name.clone(),
                        getter_expr: backing,
                        setter: None,
                        setter_expr: None,
                    });
                }
            }
            result
        }
        _ => vec![],
    }
}

// ── Awake assembly ──────────────────────────────────────────────

struct FieldInfo {
    name: String,
    ty_str: String,
}

struct PoolInfo {
    name: String,
    item_type: String,
    capacity: u32,
    max_size: u32,
    prefab_field: Option<String>,
}

fn collect_component_init(members: &[Member]) -> (Vec<FieldInfo>, Vec<FieldInfo>, Vec<FieldInfo>, Vec<FieldInfo>, Vec<PoolInfo>, Option<&Block>) {
    let mut require = Vec::new();
    let mut optional = Vec::new();
    let mut child = Vec::new();
    let mut parent = Vec::new();
    let mut pools = Vec::new();
    let mut user_awake = None;

    // First pass: collect serialize field names by type for prefab matching
    let mut serialize_by_type: HashMap<String, String> = HashMap::new();
    for m in members {
        if let Member::SerializeField { name, ty, .. } = m {
            let ty_str = lower_type(ty);
            serialize_by_type.insert(ty_str, name.clone());
        }
    }

    for m in members {
        match m {
            Member::Require { name, ty, .. } => {
                require.push(FieldInfo { name: name.clone(), ty_str: lower_type(ty) });
            }
            Member::Optional { name, ty, .. } => {
                optional.push(FieldInfo { name: name.clone(), ty_str: lower_type(ty) });
            }
            Member::Child { name, ty, .. } => {
                child.push(FieldInfo { name: name.clone(), ty_str: lower_type(ty) });
            }
            Member::Parent { name, ty, .. } => {
                parent.push(FieldInfo { name: name.clone(), ty_str: lower_type(ty) });
            }
            Member::Pool { name, item_type, capacity, max_size, .. } => {
                let item_ty_str = lower_type(item_type);
                let prefab_field = serialize_by_type.get(&item_ty_str).cloned();
                pools.push(PoolInfo {
                    name: name.clone(),
                    item_type: item_ty_str,
                    capacity: *capacity,
                    max_size: *max_size,
                    prefab_field,
                });
            }
            Member::Lifecycle { kind: LifecycleKind::Awake, body, .. } => {
                user_awake = Some(body);
            }
            _ => {}
        }
    }
    (require, optional, child, parent, pools, user_awake)
}

#[derive(Debug, Clone)]
struct CallableSignature {
    params: Vec<TypeRef>,
}

fn collect_callable_signatures(members: &[Member]) -> HashMap<String, CallableSignature> {
    let mut signatures = HashMap::new();

    for member in members {
        match member {
            Member::Func { name, params, .. } => {
                signatures.insert(
                    name.clone(),
                    CallableSignature {
                        params: params.iter().map(|param| param.ty.clone()).collect(),
                    },
                );
            }
            Member::Coroutine { name, params, .. } => {
                signatures.insert(
                    name.clone(),
                    CallableSignature {
                        params: params.iter().map(|param| param.ty.clone()).collect(),
                    },
                );
            }
            Member::IntrinsicFunc { name, params, .. } => {
                signatures.insert(
                    name.clone(),
                    CallableSignature {
                        params: params.iter().map(|param| param.ty.clone()).collect(),
                    },
                );
            }
            Member::IntrinsicCoroutine { name, params, .. } => {
                signatures.insert(
                    name.clone(),
                    CallableSignature {
                        params: params.iter().map(|param| param.ty.clone()).collect(),
                    },
                );
            }
            _ => {}
        }
    }

    signatures
}

fn lower_awake(
    component_name: &str,
    require: &[FieldInfo],
    optional: &[FieldInfo],
    child: &[FieldInfo],
    parent: &[FieldInfo],
    pools: &[PoolInfo],
    user_awake: Option<&Block>,
    callable_signatures: &HashMap<String, CallableSignature>,
) -> CsMember {
    let mut body = Vec::new();

    // Require fields
    for f in require {
        let backing = format!("_{}", f.name);
        body.push(CsStmt::Assignment {
            target: backing.clone(),
            op: "=".into(),
            value: format!("GetComponent<{}>()", f.ty_str),
            source_span: None,
        });
        body.push(CsStmt::If {
            cond: format!("{} == null", backing),
            then_body: vec![
                CsStmt::Expr(format!(
                    "Debug.LogError($\"[{}] Required component {} not found on {{gameObject.name}}\", this)",
                    component_name, f.ty_str
                ), None),
                CsStmt::Assignment { target: "enabled".into(), op: "=".into(), value: "false".into(), source_span: None },
                CsStmt::Return(None, None),
            ],
            else_body: None,
            source_span: None,
        });
    }

    // Optional fields
    for f in optional {
        body.push(CsStmt::Assignment {
            target: format!("_{}", f.name),
            op: "=".into(),
            value: format!("GetComponent<{}>()", f.ty_str),
            source_span: None,
        });
    }

    // Child fields
    for f in child {
        let backing = format!("_{}", f.name);
        body.push(CsStmt::Assignment {
            target: backing.clone(),
            op: "=".into(),
            value: format!("GetComponentInChildren<{}>()", f.ty_str),
            source_span: None,
        });
        body.push(CsStmt::If {
            cond: format!("{} == null", backing),
            then_body: vec![
                CsStmt::Expr(format!(
                    "Debug.LogError($\"[{}] Required child component {} not found on {{gameObject.name}}\", this)",
                    component_name, f.ty_str
                ), None),
                CsStmt::Assignment { target: "enabled".into(), op: "=".into(), value: "false".into(), source_span: None },
                CsStmt::Return(None, None),
            ],
            else_body: None,
            source_span: None,
        });
    }

    // Parent fields
    for f in parent {
        let backing = format!("_{}", f.name);
        body.push(CsStmt::Assignment {
            target: backing.clone(),
            op: "=".into(),
            value: format!("GetComponentInParent<{}>()", f.ty_str),
            source_span: None,
        });
        body.push(CsStmt::If {
            cond: format!("{} == null", backing),
            then_body: vec![
                CsStmt::Expr(format!(
                    "Debug.LogError($\"[{}] Required parent component {} not found on {{gameObject.name}}\", this)",
                    component_name, f.ty_str
                ), None),
                CsStmt::Assignment { target: "enabled".into(), op: "=".into(), value: "false".into(), source_span: None },
                CsStmt::Return(None, None),
            ],
            else_body: None,
            source_span: None,
        });
    }

    // Pool initialization
    for p in pools {
        let backing = format!("_{}", p.name);
        let create_func = if let Some(ref prefab) = p.prefab_field {
            format!("() => Instantiate(_{prefab})")
        } else {
            format!("() => new {}()", p.item_type)
        };
        let init_expr = format!(
            "new ObjectPool<{ty}>(\n\
             {indent}createFunc: {create},\n\
             {indent}actionOnGet: obj => obj.gameObject.SetActive(true),\n\
             {indent}actionOnRelease: obj => obj.gameObject.SetActive(false),\n\
             {indent}actionOnDestroy: obj => Destroy(obj.gameObject),\n\
             {indent}collectionCheck: true,\n\
             {indent}defaultCapacity: {cap},\n\
             {indent}maxSize: {max}\n\
             {indent2})",
            ty = p.item_type,
            create = create_func,
            cap = p.capacity,
            max = p.max_size,
            indent = "                ",
            indent2 = "            ",
        );
        body.push(CsStmt::Assignment {
            target: backing,
            op: "=".into(),
            value: init_expr,
            source_span: None,
        });
    }

    // User awake block
    if let Some(awake_block) = user_awake {
        for stmt in &awake_block.stmts {
            body.push(lower_stmt_with_context(stmt, Some(callable_signatures), None));
        }
    }

    CsMember::Method {
        attributes: vec![],
        modifiers: "private".into(),
        return_ty: "void".into(),
        name: "Awake".into(),
        params: vec![],
        where_clauses: vec![],
        body,
        source_span: None,
    }
}

