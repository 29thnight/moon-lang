// ── Statement lowering ──────────────────────────────────────────

fn stmt_span(stmt: &Stmt) -> Span {
    match stmt {
        Stmt::ValDecl { span, .. }
        | Stmt::VarDecl { span, .. }
        | Stmt::Assignment { span, .. }
        | Stmt::Expr { span, .. }
        | Stmt::If { span, .. }
        | Stmt::When { span, .. }
        | Stmt::For { span, .. }
        | Stmt::While { span, .. }
        | Stmt::Return { span, .. }
        | Stmt::Wait { span, .. }
        | Stmt::Start { span, .. }
        | Stmt::Stop { span, .. }
        | Stmt::StopAll { span, .. }
        | Stmt::Listen { span, .. }
        | Stmt::Unlisten { span, .. }
        | Stmt::DestructureVal { span, .. }
        | Stmt::IntrinsicBlock { span, .. }
        | Stmt::Break { span, .. }
        | Stmt::Continue { span, .. }
        | Stmt::Try { span, .. }
        | Stmt::Throw { span, .. } => *span,
    }
}

fn expr_span(expr: &Expr) -> Span {
    match expr {
        Expr::IntLit(_, span)
        | Expr::FloatLit(_, span)
        | Expr::DurationLit(_, span)
        | Expr::StringLit(_, span)
        | Expr::BoolLit(_, span)
        | Expr::Null(span)
        | Expr::Ident(_, span)
        | Expr::This(span) => *span,
        Expr::StringInterp { span, .. }
        | Expr::Binary { span, .. }
        | Expr::Unary { span, .. }
        | Expr::MemberAccess { span, .. }
        | Expr::SafeCall { span, .. }
        | Expr::SafeMethodCall { span, .. }
        | Expr::NonNullAssert { span, .. }
        | Expr::Elvis { span, .. }
        | Expr::Call { span, .. }
        | Expr::IndexAccess { span, .. }
        | Expr::IfExpr { span, .. }
        | Expr::WhenExpr { span, .. }
        | Expr::Range { span, .. }
        | Expr::Is { span, .. }
        | Expr::Lambda { span, .. }
        | Expr::IntrinsicExpr { span, .. } => *span,
    }
}

fn member_span(member: &Member) -> Span {
    match member {
        Member::SerializeField { span, .. }
        | Member::Field { span, .. }
        | Member::Require { span, .. }
        | Member::Optional { span, .. }
        | Member::Child { span, .. }
        | Member::Parent { span, .. }
        | Member::Func { span, .. }
        | Member::Coroutine { span, .. }
        | Member::Lifecycle { span, .. }
        | Member::IntrinsicFunc { span, .. }
        | Member::IntrinsicCoroutine { span, .. }
        | Member::Pool { span, .. } => *span,
    }
}

fn lower_stmt(stmt: &Stmt) -> CsStmt {
    lower_stmt_with_context(stmt, None, None)
}

fn lower_stmt_with_context(
    stmt: &Stmt,
    callable_signatures: Option<&HashMap<String, CallableSignature>>,
    expected_return_ty: Option<&TypeRef>,
) -> CsStmt {
    let source_span = Some(stmt_span(stmt));
    match stmt {
        Stmt::ValDecl { name, ty, init, .. } => {
            let cs_ty = ty.as_ref().map(|t| lower_type(t)).unwrap_or("var".into());
            CsStmt::VarDecl {
                ty: cs_ty,
                name: name.clone(),
                init: lower_expr_with_expected_type(init, ty.as_ref(), callable_signatures),
                source_span,
            }
        }
        Stmt::VarDecl { name, ty, init, .. } => {
            let cs_ty = ty.as_ref().map(|t| lower_type(t)).unwrap_or("var".into());
            let init_str = init
                .as_ref()
                .map(|expr| lower_expr_with_expected_type(expr, ty.as_ref(), callable_signatures))
                .unwrap_or("default".into());
            CsStmt::VarDecl { ty: cs_ty, name: name.clone(), init: init_str, source_span }
        }
        Stmt::Assignment { target, op, value, .. } => {
            let op_str = match op {
                AssignOp::Assign => "=",
                AssignOp::PlusAssign => "+=",
                AssignOp::MinusAssign => "-=",
                AssignOp::StarAssign => "*=",
                AssignOp::SlashAssign => "/=",
                AssignOp::ModAssign => "%=",
                AssignOp::NullCoalesceAssign => "??=",
            };
            CsStmt::Assignment {
                target: lower_expr_with_expected_type(target, None, callable_signatures),
                op: op_str.into(),
                value: lower_expr_with_expected_type(value, None, callable_signatures),
                source_span,
            }
        }
        Stmt::Expr { expr, .. } => {
            // Special handling for SafeMethodCall in statement context
            // Use Unity-safe null check: if (x != null) x.Method()
            if let Expr::SafeMethodCall { receiver, name, type_args, args, .. } = expr {
                let recv = lower_expr_with_expected_type(receiver, None, callable_signatures);
                let args_str = lower_call_args(Some(receiver.as_ref()), name, args, callable_signatures);
                let ta_str = lower_call_type_args(Some(receiver.as_ref()), name, type_args, None);
                let method = pascal_case(name);
                return CsStmt::If {
                    cond: format!("{} != null", recv),
                    then_body: vec![CsStmt::Expr(format!("{}.{}{}({})", recv, method, ta_str, args_str.join(", ")), source_span)],
                    else_body: None,
                    source_span,
                };
            }
            CsStmt::Expr(lower_expr_with_expected_type(expr, None, callable_signatures), source_span)
        }
        Stmt::If { cond, then_block, else_branch, .. } => {
            let else_body = else_branch.as_ref().map(|eb| match eb {
                ElseBranch::ElseBlock(block) => {
                    block
                        .stmts
                        .iter()
                        .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                        .collect()
                }
                ElseBranch::ElseIf(if_stmt) => {
                    vec![lower_stmt_with_context(if_stmt, callable_signatures, expected_return_ty)]
                }
            });
            CsStmt::If {
                cond: lower_expr_with_expected_type(cond, None, callable_signatures),
                then_body: then_block
                    .stmts
                    .iter()
                    .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                    .collect(),
                else_body,
                source_span,
            }
        }
        Stmt::When { subject, branches, .. } => {
            if let Some(subj) = subject {
                let mut cases = Vec::new();
                for b in branches {
                    let mut prefix_stmts: Vec<CsStmt> = Vec::new();
                    let pattern = match &b.pattern {
                        WhenPattern::Expression(expr) => format!("case {}:", lower_expr_with_expected_type(expr, None, callable_signatures)),
                        WhenPattern::Is(ty) => format!("case {} _:", lower_type(ty)),
                        WhenPattern::Else => "default:".into(),
                        WhenPattern::Binding { path, bindings, .. } => {
                            // Switch on enum payload — lower as `case TypeName.Variant _tmp:`
                            // and prepend binding variable declarations to body.
                            let type_name = path.join(".");
                            let tmp = format!("_prsm_m{}_{}", b.span.start.line, b.span.start.col);
                            for (i, bname) in bindings.iter().enumerate() {
                                prefix_stmts.push(CsStmt::VarDecl {
                                    ty: "var".into(),
                                    name: bname.clone(),
                                    init: format!("{}.Item{}", tmp, i + 1),
                                    source_span: Some(b.span),
                                });
                            }
                            format!("case {} {}:", type_name, tmp)
                        }
                        WhenPattern::Or { patterns, .. } => {
                            // OR pattern → multiple case labels for fall-through
                            let mut label = String::new();
                            for (i, p) in patterns.iter().enumerate() {
                                if i > 0 { label.push_str("\n"); }
                                match p {
                                    WhenPattern::Expression(expr) => label.push_str(&format!("case {}:", lower_expr_with_expected_type(expr, None, callable_signatures))),
                                    WhenPattern::Is(ty) => label.push_str(&format!("case {} _:", lower_type(ty))),
                                    WhenPattern::Binding { path, .. } => label.push_str(&format!("case {} _:", path.join("."))),
                                    _ => label.push_str("default:"),
                                }
                            }
                            label
                        }
                        WhenPattern::Range { start: range_start, end: range_end, .. } => {
                            // Range pattern → case with when guard
                            format!("case var _prsm_rv when _prsm_rv >= {} && _prsm_rv <= {}:",
                                lower_expr(range_start), lower_expr(range_end))
                        }
                    };
                    let body: Vec<CsStmt> = match &b.body {
                        WhenBody::Block(bl) => bl
                            .stmts
                            .iter()
                            .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                            .collect(),
                        WhenBody::Expr(e) => vec![CsStmt::Expr(
                            lower_expr_with_expected_type(e, None, callable_signatures),
                            Some(expr_span(e)),
                        )],
                    };
                    prefix_stmts.extend(body);
                    let mut body = prefix_stmts;
                    body.push(CsStmt::Break(Some(b.span)));
                    cases.push(CsSwitchCase { pattern, body });
                }
                CsStmt::Switch { subject: lower_expr(subj), cases, source_span }
            } else {
                // Condition-based when → if/else chain
                let mut result: Option<CsStmt> = None;
                for b in branches.iter().rev() {
                    let body: Vec<CsStmt> = match &b.body {
                        WhenBody::Block(bl) => bl
                            .stmts
                            .iter()
                            .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                            .collect(),
                        WhenBody::Expr(e) => vec![CsStmt::Expr(
                            lower_expr_with_expected_type(e, None, callable_signatures),
                            Some(expr_span(e)),
                        )],
                    };
                    match &b.pattern {
                        WhenPattern::Else => {
                            result = Some(CsStmt::Block(body, Some(b.span)));
                        }
                        WhenPattern::Expression(cond) => {
                            let cond_str = lower_expr_with_expected_type(cond, None, callable_signatures);
                            let guard_cond = if let Some(g) = &b.guard {
                                format!("{} && {}", cond_str, lower_expr_with_expected_type(g, None, callable_signatures))
                            } else {
                                cond_str
                            };
                            result = Some(CsStmt::If {
                                cond: guard_cond,
                                then_body: body,
                                else_body: result.map(|r| vec![r]),
                                source_span: Some(b.span),
                            });
                        }
                        WhenPattern::Binding { path, bindings, .. } => {
                            // Lower as: if (subject is TypeName.Variant _prsm_tmp) { var a = _prsm_tmp.Field0; var b = _prsm_tmp.Field1; ... }
                            let tmp = format!("_prsm_m{}_{}", b.span.start.line, b.span.start.col);
                            let type_name = path.join(".");
                            let bind_span = Some(b.span);
                            let mut bind_stmts: Vec<CsStmt> = bindings.iter().enumerate().map(|(i, bname)| {
                                CsStmt::VarDecl {
                                    ty: "var".into(),
                                    name: bname.clone(),
                                    init: format!("{}.Item{}", tmp, i + 1),
                                    source_span: bind_span,
                                }
                            }).collect();
                            bind_stmts.extend(body);
                            let guard_suffix = if let Some(g) = &b.guard {
                                format!(" && {}", lower_expr(g))
                            } else {
                                String::new()
                            };
                            result = Some(CsStmt::If {
                                cond: format!("{} is {} {}{}",
                                    subject_for_guard(result.as_ref()),
                                    type_name, tmp, guard_suffix),
                                then_body: bind_stmts,
                                else_body: result.map(|r| vec![r]),
                                source_span: Some(b.span),
                            });
                        }
                        WhenPattern::Is(_) => {}
                        WhenPattern::Or { patterns, .. } => {
                            // OR patterns in subject-less when → OR the conditions
                            let conds: Vec<String> = patterns.iter().filter_map(|p| {
                                if let WhenPattern::Expression(e) = p {
                                    Some(lower_expr_with_expected_type(e, None, callable_signatures))
                                } else {
                                    None
                                }
                            }).collect();
                            if !conds.is_empty() {
                                let combined_cond = conds.join(" || ");
                                result = Some(CsStmt::If {
                                    cond: combined_cond,
                                    then_body: body,
                                    else_body: result.map(|r| vec![r]),
                                    source_span: Some(b.span),
                                });
                            }
                        }
                        WhenPattern::Range { start: range_start, end: range_end, .. } => {
                            // Range in subject-less when — not typical, but handle gracefully
                            let cond_str = format!("/* range in */ {} <= {} /* not applicable */", lower_expr(range_start), lower_expr(range_end));
                            result = Some(CsStmt::If {
                                cond: cond_str,
                                then_body: body,
                                else_body: result.map(|r| vec![r]),
                                source_span: Some(b.span),
                            });
                        }
                    }
                }
                result.unwrap_or(CsStmt::Raw("// empty when".into(), source_span))
            }
        }
        Stmt::For { var_name, iterable, body, .. } => {
            // Check if iterable is a range expression
            if let Expr::Range { start, end, inclusive, step, .. } = iterable {
                let start_str = lower_expr(start);
                let end_str = lower_expr_with_expected_type(end, None, callable_signatures);
                let cond = if *inclusive {
                    format!("{} <= {}", var_name, end_str)
                } else {
                    format!("{} < {}", var_name, end_str)
                };
                let incr = if let Some(s) = step {
                    format!("{} += {}", var_name, lower_expr_with_expected_type(s, None, callable_signatures))
                } else {
                    format!("{}++", var_name)
                };
                CsStmt::For {
                    init: format!("int {} = {}", var_name, start_str),
                    cond,
                    incr,
                    body: body
                        .stmts
                        .iter()
                        .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                        .collect(),
                    source_span,
                }
            } else {
                CsStmt::ForEach {
                    ty: "var".into(),
                    name: var_name.clone(),
                    iterable: lower_expr_with_expected_type(iterable, None, callable_signatures),
                    body: body
                        .stmts
                        .iter()
                        .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                        .collect(),
                    source_span,
                }
            }
        }
        Stmt::While { cond, body, .. } => {
            CsStmt::While {
                cond: lower_expr_with_expected_type(cond, None, callable_signatures),
                body: body
                    .stmts
                    .iter()
                    .map(|stmt| lower_stmt_with_context(stmt, callable_signatures, expected_return_ty))
                    .collect(),
                source_span,
            }
        }
        Stmt::Return { value, .. } => {
            CsStmt::Return(
                value
                    .as_ref()
                    .map(|expr| lower_expr_with_expected_type(expr, expected_return_ty, callable_signatures)),
                source_span,
            )
        }
        Stmt::Wait { form, .. } => {
            match form {
                WaitForm::Duration(expr) => {
                    let val = lower_expr_with_expected_type(expr, None, callable_signatures);
                    CsStmt::YieldReturn(format!("new WaitForSeconds({})", val), source_span)
                }
                WaitForm::NextFrame => {
                    CsStmt::YieldReturn("null".into(), source_span)
                }
                WaitForm::FixedFrame => {
                    CsStmt::YieldReturn("new WaitForFixedUpdate()".into(), source_span)
                }
                WaitForm::Until(cond) => {
                    CsStmt::YieldReturn(
                        format!("new WaitUntil(() => {})", lower_expr_with_expected_type(cond, None, callable_signatures)),
                        source_span,
                    )
                }
                WaitForm::While(cond) => {
                    CsStmt::YieldReturn(
                        format!("new WaitWhile(() => {})", lower_expr_with_expected_type(cond, None, callable_signatures)),
                        source_span,
                    )
                }
            }
        }
        Stmt::Start { call, .. } => {
            CsStmt::Expr(
                format!("StartCoroutine({})", lower_expr_with_expected_type(call, None, callable_signatures)),
                source_span,
            )
        }
        Stmt::Stop { target, .. } => {
            CsStmt::Expr(
                format!("StopCoroutine({})", lower_expr_with_expected_type(target, None, callable_signatures)),
                source_span,
            )
        }
        Stmt::StopAll { .. } => {
            CsStmt::Expr("StopAllCoroutines()".into(), source_span)
        }
        Stmt::Listen { event, params, body, lifetime, bound_name, .. } => {
            lower_listen_stmt(
                event,
                params,
                body,
                lifetime,
                bound_name.as_deref(),
                source_span,
                None,
                callable_signatures,
            )
        }
        Stmt::Unlisten { token, .. } => {
            // Without surrounding context we cannot resolve the token.
            // Emit a TODO comment; resolved versions are emitted by lower_decl Component path.
            CsStmt::Raw(
                format!("/* unlisten {} — resolved at component level */", token),
                source_span,
            )
        }
        Stmt::IntrinsicBlock { code, .. } => {
            CsStmt::Raw(code.clone(), source_span)
        }
        Stmt::Break { .. } => CsStmt::Break(source_span),
        Stmt::Continue { .. } => CsStmt::Continue(source_span),
        Stmt::DestructureVal { pattern, init, .. } => {
            lower_destructure_val(pattern, init, source_span)
        }
        Stmt::Try { try_block, catches, finally_block, .. } => {
            let try_body: Vec<CsStmt> = try_block.stmts.iter()
                .map(|s| lower_stmt_with_context(s, callable_signatures, expected_return_ty))
                .collect();
            let cs_catches: Vec<CsCatchClause> = catches.iter()
                .map(|c| CsCatchClause {
                    exception_type: lower_type(&c.ty),
                    name: c.name.clone(),
                    body: c.body.stmts.iter()
                        .map(|s| lower_stmt_with_context(s, callable_signatures, expected_return_ty))
                        .collect(),
                })
                .collect();
            let finally_body = finally_block.as_ref().map(|fb|
                fb.stmts.iter()
                    .map(|s| lower_stmt_with_context(s, callable_signatures, expected_return_ty))
                    .collect()
            );
            CsStmt::TryCatch {
                try_body,
                catches: cs_catches,
                finally_body,
                source_span,
            }
        }
        Stmt::Throw { expr, .. } => {
            // PrSM: throw Exception("msg") → C#: throw new Exception("msg")
            let expr_str = lower_expr_with_expected_type(expr, None, callable_signatures);
            CsStmt::Throw(format!("new {}", expr_str), source_span)
        }
    }
}

fn stmt_to_inline(stmt: &CsStmt) -> String {
    match stmt {
        CsStmt::Expr(e, _) => e.clone(),
        CsStmt::Return(Some(v), _) => format!("return {}", v),
        CsStmt::Return(None, _) => "return".into(),
        _ => format!("{:?}", stmt),
    }
}

#[allow(dead_code)]
fn lower_value_block_expr(block: &Block, context: &str) -> String {
    lower_value_block_expr_with_expected(block, context, None, None)
}

fn lower_value_block_expr_with_expected(
    block: &Block,
    context: &str,
    expected_type: Option<&TypeRef>,
    callable_signatures: Option<&HashMap<String, CallableSignature>>,
) -> String {
    if block.stmts.len() == 1 {
        match &block.stmts[0] {
            Stmt::Expr { expr, .. } => return lower_expr_with_expected_type(expr, expected_type, callable_signatures),
            Stmt::Return { value: Some(expr), .. } => {
                return lower_expr_with_expected_type(expr, expected_type, callable_signatures)
            }
            _ => {}
        }
    }

    let mut lines = vec!["__prsm_expr(() =>".to_string(), "{".to_string()];
    lines.extend(lower_value_block_lines_with_expected(
        block,
        1,
        context,
        expected_type,
        callable_signatures,
    ));
    lines.push("})".to_string());
    lines.join("\n")
}

#[allow(dead_code)]
fn lower_value_block_lines(block: &Block, indent: usize, context: &str) -> Vec<String> {
    lower_value_block_lines_with_expected(block, indent, context, None, None)
}

fn lower_value_block_lines_with_expected(
    block: &Block,
    indent: usize,
    context: &str,
    expected_type: Option<&TypeRef>,
    callable_signatures: Option<&HashMap<String, CallableSignature>>,
) -> Vec<String> {
    let mut lines = Vec::new();
    if block.stmts.is_empty() {
        lines.push(format!("{}throw new System.InvalidOperationException(\"PrSM {} branch must produce a value\");", indent_str(indent), context));
        return lines;
    }

    let last_index = block.stmts.len() - 1;
    for stmt in &block.stmts[..last_index] {
        lines.extend(cs_stmt_to_lines(
            &lower_stmt_with_context(stmt, callable_signatures, expected_type),
            indent,
        ));
    }

    match &block.stmts[last_index] {
        Stmt::Expr { expr, .. } => lines.push(format!(
            "{}return {};",
            indent_str(indent),
            lower_expr_with_expected_type(expr, expected_type, callable_signatures)
        )),
        Stmt::Return { value: Some(expr), .. } => lines.push(format!(
            "{}return {};",
            indent_str(indent),
            lower_expr_with_expected_type(expr, expected_type, callable_signatures)
        )),
        Stmt::Return { value: None, .. } => lines.push(format!("{}return default;", indent_str(indent))),
        other => {
            lines.extend(cs_stmt_to_lines(
                &lower_stmt_with_context(other, callable_signatures, expected_type),
                indent,
            ));
            lines.push(format!("{}throw new System.InvalidOperationException(\"PrSM {} branch must end with a value\");", indent_str(indent), context));
        }
    }

    lines
}

fn cs_stmt_to_lines(stmt: &CsStmt, indent: usize) -> Vec<String> {
    let pad = indent_str(indent);
    match stmt {
        CsStmt::VarDecl { ty, name, init, .. } => vec![format!("{}{} {} = {};", pad, ty, name, init)],
        CsStmt::Assignment { target, op, value, .. } => vec![format!("{}{} {} {};", pad, target, op, value)],
        CsStmt::Expr(expr, _) => vec![format!("{}{};", pad, expr)],
        CsStmt::If { cond, then_body, else_body, .. } => {
            let mut lines = vec![format!("{}if ({})", pad, cond), format!("{}{{", pad)];
            for nested in then_body {
                lines.extend(cs_stmt_to_lines(nested, indent + 1));
            }
            lines.push(format!("{}}}", pad));
            if let Some(else_stmts) = else_body {
                lines.push(format!("{}else", pad));
                lines.push(format!("{}{{", pad));
                for nested in else_stmts {
                    lines.extend(cs_stmt_to_lines(nested, indent + 1));
                }
                lines.push(format!("{}}}", pad));
            }
            lines
        }
        CsStmt::Switch { subject, cases, .. } => {
            let mut lines = vec![format!("{}switch ({})", pad, subject), format!("{}{{", pad)];
            for case in cases {
                lines.push(format!("{}    {}", pad, case.pattern));
                for nested in &case.body {
                    lines.extend(cs_stmt_to_lines(nested, indent + 2));
                }
            }
            lines.push(format!("{}}}", pad));
            lines
        }
        CsStmt::For { init, cond, incr, body, .. } => {
            let mut lines = vec![format!("{}for ({}; {}; {})", pad, init, cond, incr), format!("{}{{", pad)];
            for nested in body {
                lines.extend(cs_stmt_to_lines(nested, indent + 1));
            }
            lines.push(format!("{}}}", pad));
            lines
        }
        CsStmt::ForEach { ty, name, iterable, body, .. } => {
            let mut lines = vec![format!("{}foreach ({} {} in {})", pad, ty, name, iterable), format!("{}{{", pad)];
            for nested in body {
                lines.extend(cs_stmt_to_lines(nested, indent + 1));
            }
            lines.push(format!("{}}}", pad));
            lines
        }
        CsStmt::While { cond, body, .. } => {
            let mut lines = vec![format!("{}while ({})", pad, cond), format!("{}{{", pad)];
            for nested in body {
                lines.extend(cs_stmt_to_lines(nested, indent + 1));
            }
            lines.push(format!("{}}}", pad));
            lines
        }
        CsStmt::Return(Some(value), _) => vec![format!("{}return {};", pad, value)],
        CsStmt::Return(None, _) => vec![format!("{}return;", pad)],
        CsStmt::YieldReturn(value, _) => vec![format!("{}yield return {};", pad, value)],
        CsStmt::Break(_) => vec![format!("{}break;", pad)],
        CsStmt::Continue(_) => vec![format!("{}continue;", pad)],
        CsStmt::Raw(code, _) => code.lines().map(|line| format!("{}{}", pad, line)).collect(),
        CsStmt::Block(stmts, _) => {
            let mut lines = Vec::new();
            for nested in stmts {
                lines.extend(cs_stmt_to_lines(nested, indent));
            }
            lines
        }
        CsStmt::TryCatch { try_body, catches, finally_body, .. } => {
            let mut lines = vec![format!("{}try", pad), format!("{}{{", pad)];
            for nested in try_body {
                lines.extend(cs_stmt_to_lines(nested, indent + 1));
            }
            lines.push(format!("{}}}", pad));
            for c in catches {
                lines.push(format!("{}catch ({} {})", pad, c.exception_type, c.name));
                lines.push(format!("{}{{", pad));
                for nested in &c.body {
                    lines.extend(cs_stmt_to_lines(nested, indent + 1));
                }
                lines.push(format!("{}}}", pad));
            }
            if let Some(finally_stmts) = finally_body {
                lines.push(format!("{}finally", pad));
                lines.push(format!("{}{{", pad));
                for nested in finally_stmts {
                    lines.extend(cs_stmt_to_lines(nested, indent + 1));
                }
                lines.push(format!("{}}}", pad));
            }
            lines
        }
        CsStmt::Throw(expr, _) => vec![format!("{}throw {};", pad, expr)],
    }
}

fn indent_str(indent: usize) -> String {
    "    ".repeat(indent)
}

// ── Expression lowering ─────────────────────────────────────────

fn lower_expr(expr: &Expr) -> String {
    lower_expr_with_expected_type(expr, None, None)
}

fn lower_expr_with_expected_type(
    expr: &Expr,
    expected_type: Option<&TypeRef>,
    callable_signatures: Option<&HashMap<String, CallableSignature>>,
) -> String {
    match expr {
        Expr::IntLit(n, _) => n.to_string(),
        Expr::FloatLit(n, _) => format_float(*n),
        Expr::DurationLit(n, _) => format_float(*n),
        Expr::StringLit(s, _) => format!("\"{}\"", s),
        Expr::StringInterp { parts, .. } => {
            let mut result = String::from("$\"");
            for part in parts {
                match part {
                    StringPart::Literal(s) => result.push_str(s),
                    StringPart::Expr(e) => {
                        result.push('{');
                        result.push_str(&lower_expr_with_expected_type(e, None, callable_signatures));
                        result.push('}');
                    }
                }
            }
            result.push('"');
            result
        }
        Expr::BoolLit(b, _) => if *b { "true" } else { "false" }.into(),
        Expr::Null(_) => "null".into(),
        Expr::Ident(name, _) => map_sugar_ident(name),
        Expr::This(_) => "this".into(),
        Expr::Binary { left, op, right, .. } => {
            if *op == BinOp::In {
                // `x in range` or `x in collection`
                let lhs = lower_expr_with_expected_type(left, None, callable_signatures);
                return match right.as_ref() {
                    Expr::Range { start: range_start, end: range_end, .. } => {
                        let lo = lower_expr_with_expected_type(range_start, None, callable_signatures);
                        let hi = lower_expr_with_expected_type(range_end, None, callable_signatures);
                        format!("{} >= {} && {} <= {}", lhs, lo, lhs, hi)
                    }
                    _ => {
                        let rhs = lower_expr_with_expected_type(right, None, callable_signatures);
                        format!("{}.Contains({})", rhs, lhs)
                    }
                };
            }
            let op_str = match op {
                BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
                BinOp::Div => "/", BinOp::Mod => "%",
                BinOp::Eq => "==", BinOp::NotEq => "!=",
                BinOp::Lt => "<", BinOp::Gt => ">",
                BinOp::LtEq => "<=", BinOp::GtEq => ">=",
                BinOp::And => "&&", BinOp::Or => "||",
                BinOp::In => unreachable!("handled above"),
            };
            format!(
                "{} {} {}",
                lower_expr_with_expected_type(left, None, callable_signatures),
                op_str,
                lower_expr_with_expected_type(right, None, callable_signatures),
            )
        }
        Expr::Unary { op, operand, .. } => {
            match op {
                UnaryOp::Negate => format!("-({})", lower_expr_with_expected_type(operand, None, callable_signatures)),
                UnaryOp::Not => format!("!({})", lower_expr_with_expected_type(operand, None, callable_signatures)),
            }
        }
        Expr::MemberAccess { receiver, name, .. } => {
            // New Input System sugar: input.action("X").pressed → _prsmInput.actions["X"].WasPressedThisFrame()
            if let Some(cs) = try_lower_new_input_member(receiver, name) {
                return cs;
            }
            let recv = lower_expr_with_expected_type(receiver, None, callable_signatures);
            // Legacy input + other sugar mappings
            match (recv.as_str(), name.as_str()) {
                ("input", "axis") => "Input.GetAxis".into(),
                ("input", "getKey") => "Input.GetKey".into(),
                ("input", "getKeyDown") => "Input.GetKeyDown".into(),
                ("input", "getButton") => "Input.GetButton".into(),
                ("input", "getButtonDown") => "Input.GetButtonDown".into(),
                ("quat", "euler") => "Quaternion.Euler".into(),
                ("Time", "deltaTime") => "Time.deltaTime".into(),
                // Duration sugar: expr.s → just expr (seconds value)
                (_, "s") => recv.to_string(),
                _ => format!("{}.{}", recv, name),
            }
        }
        Expr::SafeCall { receiver, name, .. } => {
            let recv = lower_expr_with_expected_type(receiver, None, callable_signatures);
            format!("({} != null ? {}.{} : null)", recv, recv, pascal_case(name))
        }
        Expr::SafeMethodCall { receiver, name, type_args, args, .. } => {
            // SafeMethodCall in expression context — use null-conditional operator
            let recv = lower_expr_with_expected_type(receiver, None, callable_signatures);
            let args_str = lower_call_args(Some(receiver.as_ref()), name, args, callable_signatures);
            let ta_str = lower_call_type_args(Some(receiver.as_ref()), name, type_args, expected_type);
            let method = pascal_case(name);
            format!("{}?.{}{}({})", recv, method, ta_str, args_str.join(", "))
        }
        Expr::NonNullAssert { expr, .. } => {
            lower_expr_with_expected_type(expr, expected_type, callable_signatures)
        }
        Expr::Elvis { left, right, .. } => {
            format!(
                "{} ?? {}",
                lower_expr_with_expected_type(left, expected_type, callable_signatures),
                lower_expr_with_expected_type(right, expected_type, callable_signatures),
            )
        }
        Expr::Call { receiver, name, type_args, args, .. } => {
            let args_str = lower_call_args(receiver.as_deref(), name, args, callable_signatures);
            let ta_str = lower_call_type_args(receiver.as_deref(), name, type_args, expected_type);

            if let Some(recv) = receiver {
                let recv_str = lower_expr_with_expected_type(recv, None, callable_signatures);
                // Handle safe call + method call (SafeCall node as receiver)
                if name.is_empty() {
                    format!("{}{}({})", recv_str, ta_str, args_str.join(", "))
                } else {
                    // Check for sugar: input.axis → Input.GetAxis, etc.
                    if let Some(mapped) = map_method_sugar(&recv_str, name) {
                        format!("{}{}({})", mapped, ta_str, args_str.join(", "))
                    } else {
                        // PascalCase for C# method calls
                        format!("{}.{}{}({})", recv_str, pascal_case(name), ta_str, args_str.join(", "))
                    }
                }
            } else {
                // Sugar function calls
                let mapped = map_sugar_call(name, &ta_str, &args_str);
                if let Some(m) = mapped {
                    m
                } else {
                    format!("{}{}({})", name, ta_str, args_str.join(", "))
                }
            }
        }
        Expr::IndexAccess { receiver, index, .. } => {
            format!(
                "{}[{}]",
                lower_expr_with_expected_type(receiver, None, callable_signatures),
                lower_expr_with_expected_type(index, None, callable_signatures),
            )
        }
        Expr::Range { start, .. } => {
            // Ranges are handled in for-loop lowering; this is a fallback
            lower_expr_with_expected_type(start, None, callable_signatures)
        }
        Expr::Is { expr, ty, .. } => {
            format!(
                "{} is {}",
                lower_expr_with_expected_type(expr, None, callable_signatures),
                lower_type(ty)
            )
        }
        Expr::IfExpr { cond, then_block, else_block, .. } => {
            format!(
                "({} ? {} : {})",
                lower_expr_with_expected_type(cond, None, callable_signatures),
                lower_value_block_expr_with_expected(then_block, "if expression", expected_type, callable_signatures),
                lower_value_block_expr_with_expected(else_block, "if expression", expected_type, callable_signatures),
            )
        }
        Expr::WhenExpr { subject, branches, .. } => {
            if let Some(subj) = subject {
                let mut lines = vec![format!(
                    "{} switch",
                    lower_expr_with_expected_type(subj, None, callable_signatures)
                ), "{".to_string()];
                for branch in branches {
                    let pattern = match &branch.pattern {
                        WhenPattern::Expression(expr) => lower_expr_with_expected_type(expr, None, callable_signatures),
                        WhenPattern::Is(ty) => format!("{} _", lower_type(ty)),
                        WhenPattern::Else => "_".into(),
                        WhenPattern::Binding { path, .. } => format!("{} _", path.join(".")),
                        WhenPattern::Or { patterns, .. } => {
                            patterns.iter().filter_map(|p| match p {
                                WhenPattern::Expression(e) => Some(lower_expr_with_expected_type(e, None, callable_signatures)),
                                WhenPattern::Is(ty) => Some(format!("{} _", lower_type(ty))),
                                WhenPattern::Binding { path, .. } => Some(format!("{} _", path.join("."))),
                                _ => None,
                            }).collect::<Vec<_>>().join(" or ")
                        }
                        WhenPattern::Range { start: range_start, end: range_end, .. } => {
                            format!(">= {} and <= {}", lower_expr(range_start), lower_expr(range_end))
                        }
                    };
                    let value = match &branch.body {
                        WhenBody::Expr(expr) => lower_expr_with_expected_type(expr, expected_type, callable_signatures),
                        WhenBody::Block(block) => lower_value_block_expr_with_expected(
                            block,
                            "when expression",
                            expected_type,
                            callable_signatures,
                        ),
                    };
                    lines.push(format!("    {} => {},", pattern, value));
                }
                if !branches.iter().any(|branch| matches!(branch.pattern, WhenPattern::Else)) {
                    lines.push("    _ => throw new System.InvalidOperationException(\"PrSM when expression is not exhaustive\"),".to_string());
                }
                lines.push("}".to_string());
                lines.join("\n")
            } else {
                let mut result = "throw new System.InvalidOperationException(\"PrSM when expression did not match any branch\")".to_string();
                for branch in branches.iter().rev() {
                    let value = match &branch.body {
                        WhenBody::Expr(expr) => lower_expr_with_expected_type(expr, expected_type, callable_signatures),
                        WhenBody::Block(block) => lower_value_block_expr_with_expected(
                            block,
                            "when expression",
                            expected_type,
                            callable_signatures,
                        ),
                    };
                    result = match &branch.pattern {
                        WhenPattern::Else => value,
                        WhenPattern::Expression(cond) => format!(
                            "({} ? {} : {})",
                            lower_expr_with_expected_type(cond, None, callable_signatures),
                            value,
                            result
                        ),
                        WhenPattern::Is(_) | WhenPattern::Binding { .. }
                        | WhenPattern::Or { .. } | WhenPattern::Range { .. } => result,
                    };
                }
                result
            }
        }
        Expr::Lambda { .. } => {
            "/* lambda */".into()
        }
        Expr::IntrinsicExpr { code, .. } => code.clone(),
    }
}

fn lower_call_args(
    receiver: Option<&Expr>,
    name: &str,
    args: &[Arg],
    callable_signatures: Option<&HashMap<String, CallableSignature>>,
) -> Vec<String> {
    let signature = lookup_callable_signature(receiver, name, callable_signatures);
    args
        .iter()
        .enumerate()
        .map(|(index, arg)| {
            let expected_type = signature.and_then(|sig| sig.params.get(index));
            lower_expr_with_expected_type(&arg.value, expected_type, callable_signatures)
        })
        .collect()
}

fn lower_call_type_args(
    receiver: Option<&Expr>,
    name: &str,
    type_args: &[TypeRef],
    expected_type: Option<&TypeRef>,
) -> String {
    if !type_args.is_empty() {
        return format!(
            "<{}>",
            type_args
                .iter()
                .map(lower_type)
                .collect::<Vec<_>>()
                .join(", ")
        );
    }

    if !supports_expected_type_inference(receiver, name) {
        return String::new();
    }

    let Some(expected_type) = expected_type else {
        return String::new();
    };

    let inferred = lower_type(&strip_nullable_type(expected_type));
    if inferred == "void" {
        String::new()
    } else {
        format!("<{}>", inferred)
    }
}

fn lookup_callable_signature<'a>(
    receiver: Option<&Expr>,
    name: &str,
    callable_signatures: Option<&'a HashMap<String, CallableSignature>>,
) -> Option<&'a CallableSignature> {
    let signatures = callable_signatures?;
    match receiver {
        None => signatures.get(name),
        Some(Expr::This(_)) => signatures.get(name),
        _ => None,
    }
}

fn supports_expected_type_inference(receiver: Option<&Expr>, name: &str) -> bool {
    match receiver {
        None => matches!(name, "get" | "require" | "find" | "child" | "parent" | "loadJson"),
        Some(_) => matches!(
            name,
            "getComponent" | "getComponentInChildren" | "getComponentInParent" | "findFirstObjectByType"
        ),
    }
}

fn strip_nullable_type(ty: &TypeRef) -> TypeRef {
    match ty {
        TypeRef::Simple { name, span, .. } => TypeRef::Simple {
            name: name.clone(),
            nullable: false,
            span: *span,
        },
        TypeRef::Generic { name, type_args, span, .. } => TypeRef::Generic {
            name: name.clone(),
            type_args: type_args.clone(),
            nullable: false,
            span: *span,
        },
        TypeRef::Qualified { qualifier, name, span, .. } => TypeRef::Qualified {
            qualifier: qualifier.clone(),
            name: name.clone(),
            nullable: false,
            span: *span,
        },
    }
}

// ── Sugar mappings ──────────────────────────────────────────────

fn map_sugar_ident(name: &str) -> String {
    match name {
        "input" => "Input".into(),
        _ => name.into(),
    }
}

fn map_sugar_call(name: &str, type_args: &str, args: &[String]) -> Option<String> {
    let args_str = args.join(", ");
    match name {
        "vec2" => Some(format!("new Vector2({})", args_str)),
        "vec3" => Some(format!("new Vector3({})", args_str)),
        "color" => Some(format!("new Color({})", args_str)),
        "get" => Some(format!("GetComponent{}()", type_args)),
        "require" => Some(format!("GetComponent{}()", type_args)),
        "find" => Some(format!("FindFirstObjectByType{}()", type_args)),
        "child" => Some(format!("GetComponentInChildren{}()", type_args)),
        "parent" => Some(format!("GetComponentInParent{}()", type_args)),
        "Destroy" => Some(format!("Destroy({})", args_str)),
        "log" => Some(format!("Debug.Log({})", args_str)),
        "warn" => Some(format!("Debug.LogWarning({})", args_str)),
        "error" => Some(format!("Debug.LogError({})", args_str)),
        "print" => {
            // print("msg") → Debug.Log
            // print("msg", Log) → Debug.Log
            // print("msg", Warn) → Debug.LogWarning
            // print("msg", Error) → Debug.LogError
            if args.len() >= 2 {
                let level = args[1].trim();
                let msg = &args[0];
                match level {
                    "Warn" => Some(format!("Debug.LogWarning({})", msg)),
                    "Error" => Some(format!("Debug.LogError({})", msg)),
                    _ => Some(format!("Debug.Log({})", msg)),
                }
            } else {
                Some(format!("Debug.Log({})", args_str))
            }
        }
        _ => None,
    }
}

// ── Type lowering ───────────────────────────────────────────────

fn lower_type(ty: &TypeRef) -> String {
    match ty {
        TypeRef::Simple { name, nullable, .. } => {
            let cs = map_type_name(name);
            if *nullable { format!("{}?", cs) } else { cs }
        }
        TypeRef::Generic { name, type_args, nullable, .. } => {
            let args: Vec<String> = type_args.iter().map(|t| lower_type(t)).collect();
            // Array<T> → T[]
            let base = if name == "Array" && args.len() == 1 {
                format!("{}[]", args[0])
            } else {
                let cs_name = map_type_name(name);
                format!("{}<{}>", cs_name, args.join(", "))
            };
            if *nullable { format!("{}?", base) } else { base }
        }
        TypeRef::Qualified { qualifier, name, nullable, .. } => {
            let base = format!("{}.{}", qualifier, name);
            if *nullable { format!("{}?", base) } else { base }
        }
    }
}

fn map_type_name(name: &str) -> String {
    match name {
        "Int" => "int".into(),
        "Float" => "float".into(),
        "Double" => "double".into(),
        "Bool" => "bool".into(),
        "String" => "string".into(),
        "Char" => "char".into(),
        "Long" => "long".into(),
        "Byte" => "byte".into(),
        "Unit" => "void".into(),
        // Collection sugar
        "Array" => "System.Array".into(),
        "List" => "System.Collections.Generic.List".into(),
        "Map" => "System.Collections.Generic.Dictionary".into(),
        "Set" => "System.Collections.Generic.HashSet".into(),
        "Queue" => "System.Collections.Generic.Queue".into(),
        "Stack" => "System.Collections.Generic.Stack".into(),
        "Seq" => "System.Collections.Generic.IEnumerable".into(),
        _ => name.into(),
    }
}

fn lower_annotation(ann: &Annotation) -> String {
    if ann.args.is_empty() {
        format!("[{}]", capitalize(&ann.name))
    } else {
        let args: Vec<String> = ann.args.iter().map(|a| lower_expr(a)).collect();
        format!("[{}({})]", capitalize(&ann.name), args.join(", "))
    }
}

fn lower_visibility(vis: Visibility) -> String {
    match vis {
        Visibility::Public => "public".into(),
        Visibility::Private => "private".into(),
        Visibility::Protected => "protected".into(),
    }
}

fn map_method_sugar(receiver: &str, method: &str) -> Option<String> {
    match (receiver, method) {
        ("Input", "axis") | ("input", "axis") => Some("Input.GetAxis".into()),
        ("Input", "getKey") | ("input", "getKey") => Some("Input.GetKey".into()),
        ("Input", "getKeyDown") | ("input", "getKeyDown") => Some("Input.GetKeyDown".into()),
        ("Input", "getButton") | ("input", "getButton") => Some("Input.GetButton".into()),
        ("Input", "getButtonDown") | ("input", "getButtonDown") => Some("Input.GetButtonDown".into()),
        ("quat", "euler") => Some("Quaternion.Euler".into()),
        _ => None,
    }
}

/// Convert camelCase method name to PascalCase for C# Unity API.
fn pascal_case(name: &str) -> String {
    let mut chars = name.chars();
    match chars.next() {
        None => String::new(),
        Some(c) => c.to_uppercase().collect::<String>() + chars.as_str(),
    }
}

fn format_float(n: f64) -> String {
    let s = n.to_string();
    if s.contains('.') {
        format!("{}f", s)
    } else {
        format!("{}.0f", s)
    }
}

fn capitalize(s: &str) -> String {
    let mut c = s.chars();
    match c.next() {
        None => String::new(),
        Some(f) => f.to_uppercase().collect::<String>() + c.as_str(),
    }
}

