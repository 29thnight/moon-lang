/// Lower a list of `WhereClause` into C# where clause strings.
fn lower_where_clauses(clauses: &[WhereClause]) -> Vec<String> {
    clauses
        .iter()
        .map(|wc| format!("where {} : {}", wc.type_param, wc.constraints.join(", ")))
        .collect()
}

