#[allow(dead_code)]
fn lower_lifecycle(kind: LifecycleKind, params: &[Param], body_block: &Block) -> CsMember {
    let method_name = lifecycle_method_name(kind);
    let cs_params: Vec<CsParam> = params.iter().map(cs_param_from).collect();
    let body: Vec<CsStmt> = body_block.stmts.iter().map(|s| lower_stmt(s)).collect();
    CsMember::Method {
        attributes: vec![],
        modifiers: "private".into(),
        return_ty: "void".into(),
        name: method_name.into(),
        params: cs_params,
        where_clauses: vec![],
        body,
        source_span: Some(body_block.span),
    }
}

fn lower_lifecycle_with_ctx(
    kind: LifecycleKind,
    params: &[Param],
    body_block: &Block,
    ctx: &mut ComponentCtx,
) -> CsMember {
    let method_name = lifecycle_method_name(kind);
    let cs_params: Vec<CsParam> = params.iter().map(cs_param_from).collect();
    let body = lower_stmts_with_ctx(&body_block.stmts, ctx, None);
    CsMember::Method {
        attributes: vec![],
        modifiers: "private".into(),
        return_ty: "void".into(),
        name: method_name.into(),
        params: cs_params,
        where_clauses: vec![],
        body,
        source_span: Some(body_block.span),
    }
}

fn lifecycle_method_name(kind: LifecycleKind) -> &'static str {
    match kind {
        LifecycleKind::Start => "Start",
        LifecycleKind::Update => "Update",
        LifecycleKind::FixedUpdate => "FixedUpdate",
        LifecycleKind::LateUpdate => "LateUpdate",
        LifecycleKind::OnEnable => "OnEnable",
        LifecycleKind::OnDisable => "OnDisable",
        LifecycleKind::OnDestroy => "OnDestroy",
        LifecycleKind::OnTriggerEnter => "OnTriggerEnter",
        LifecycleKind::OnTriggerExit => "OnTriggerExit",
        LifecycleKind::OnTriggerStay => "OnTriggerStay",
        LifecycleKind::OnCollisionEnter => "OnCollisionEnter",
        LifecycleKind::OnCollisionExit => "OnCollisionExit",
        LifecycleKind::OnCollisionStay => "OnCollisionStay",
        LifecycleKind::Awake => "Awake",
    }
}

// ── Function lowering ───────────────────────────────────────────

fn lower_func_member(m: &Member, callable_signatures: &HashMap<String, CallableSignature>) -> CsMember {
    match m {
        Member::Func { visibility, is_static, is_override, is_abstract, is_open, is_async, name, type_params, where_clauses, params, return_ty, body, .. } => {
            let inner_ret = return_ty.as_ref().map(|t| lower_type(t)).unwrap_or("void".into());
            let ret = if *is_async {
                async_return_type(&inner_ret)
            } else {
                inner_ret.clone()
            };
            let ps: Vec<CsParam> = params.iter().map(cs_param_from).collect();

            let mut mods = lower_visibility(*visibility);
            if *is_static {
                mods = format!("{} static", mods);
            }
            if *is_async {
                mods = format!("{} async", mods);
            }
            if *is_abstract {
                mods = format!("{} abstract", mods);
            } else if *is_open {
                mods = format!("{} virtual", mods);
            } else if *is_override {
                mods = format!("{} override", mods);
            }

            // Abstract functions emit signature only (no body)
            if *is_abstract {
                let cs_name = lower_func_name_with_generics(name, type_params);
                return CsMember::RawCode(format!(
                    "{} {} {}({});",
                    mods,
                    ret,
                    cs_name,
                    ps.iter().map(|p| format!("{}{} {}", p.modifier.prefix(), p.ty, p.name)).collect::<Vec<_>>().join(", ")
                ));
            }

            let cs_body = match body {
                FuncBody::Block(block) => {
                    block.stmts
                        .iter()
                        .map(|stmt| lower_stmt_with_context(stmt, Some(callable_signatures), return_ty.as_ref()))
                        .collect()
                }
                FuncBody::ExprBody(expr) => {
                    vec![CsStmt::Return(
                        Some(lower_expr_with_expected_type(expr, return_ty.as_ref(), Some(callable_signatures))),
                        Some(expr_span(expr)),
                    )]
                }
            };

            // Build method name with optional type parameters
            let cs_name = lower_func_name_with_generics(name, type_params);

            CsMember::Method {
                attributes: vec![],
                modifiers: mods,
                return_ty: ret,
                name: cs_name,
                params: ps,
                where_clauses: lower_where_clauses(where_clauses),
                body: cs_body,
                source_span: Some(member_span(m)),
            }
        }
        _ => CsMember::RawCode("// unexpected member".into()),
    }
}

/// Map a PrSM declared return type (already lowered to a C# type) to the
/// corresponding async wrapper (`Cysharp.Threading.Tasks.UniTask<T>` when the
/// UniTask dependency is present — falling back to `System.Threading.Tasks.Task`).
///
/// Currently the compiler does not yet inspect the project graph to detect
/// UniTask; we prefer `UniTask` by default since the target platform is Unity.
fn async_return_type(inner: &str) -> String {
    let trimmed = inner.trim();
    if trimmed.is_empty() || trimmed == "void" {
        "Cysharp.Threading.Tasks.UniTask".into()
    } else {
        format!("Cysharp.Threading.Tasks.UniTask<{}>", trimmed)
    }
}

